    private void btnPayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPayActionPerformed
        AccountParser ap = new AccountParser("");
        int emptycount =0;
        message1.setText("");
        if(acctnum.getText().isEmpty()){emptycount++;message1.setText("Please type in your acount number!");}      
        if(emptycount==0){
            int acct = Integer.parseInt(this.acctnum.getText());
            int acctType = ap.getAccountTypeByAccountNumber(acct);
            if(acctType==5||acctType==6){
                LoanAccounts loan = ap.getLoanAccount(acctType);
                message1.setText(loan.MakePayment());
                ap.WriteFile();
            }else{
                if(acctType==-1){
                    message1.setText("No account returned.");
                }else{
                    message1.setText("Not a Loan Account.");
                }
            }
        }//end of retrieving bill
    }//GEN-LAST:event_btnPayActionPerformed

    private void btnCCpayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCCpayActionPerformed
        // Change all Checking account interest rate
        AccountParser ap = new AccountParser("");
        int emptycount = 0;
        message2.setText("");
        if(this.ccnum.getText().isEmpty()){emptycount++;message2.setText("Please type in your acount number!");}
        if(this.amttopay.getText().isEmpty()){emptycount++;message2.setText("Please type in Amount you want to pay");}  
        if(emptycount==0){
            int acct = Integer.parseInt(this.ccnum.getText());
            double btp = Double.parseDouble(this.amttopay.getText());
            int acctType = ap.getAccountTypeByAccountNumber(acct);
            if(acctType==7){
                //pay
                CreditCards cc = ap.getCreditCardAccount(acct);
                String paystr = cc.MakePayment(btp);
                ap.WriteFile();
                message2.setText(paystr);
            }else{
                if(acctType==-1){
                    //No account returned
                    message2.setText("No Account Returned.");
                }else{
                    //Wrong account
                    message2.setText("Not a credit account.");
                }
            }
        }//end of retrieving bill
        //ap.SetCheckingAndSavingIntrestRates(WIDTH)
    }//GEN-LAST:event_btnCCpayActionPerformed

    private void acctnumActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acctnumActionPerformed
        AccountParser ap = new AccountParser("");
        int emptycount =0;
        message1.setText("");
        if(acctnum.getText().isEmpty()){emptycount++;message1.setText("Please type in your acount number!");return;}      
        if(emptycount==0){
            int acct = Integer.parseInt(this.acctnum.getText());
            int acctType = ap.getAccountTypeByAccountNumber(acct);
            if(acctType==5||acctType==6){
                LoanAccounts loan = ap.getLoanAccount(acct);
                String billstr = String.valueOf(loan.getBillamount());
                billdue.setText(billstr);
                message1.setText("See your bill above");
                return;
            }else{
                if(acctType==-1){
                    message1.setText("No account returned.");
                    return;
                }else{
                    message1.setText("Not a Loan Account.");
                    return;
                }
            }
        }//end of retrieving bill
    }//GEN-LAST:event_acctnumActionPerformed

